      DOUBLE PRECISION FUNCTION D1MACH(I)

C=====================================================
      INTEGER I
      IF (I.EQ.1) THEN
         D1MACH = 2.2250739D-308
      ELSEIF (I.EQ.2) THEN
         D1MACH = 1.7976931D+308
      ELSEIF (I.EQ.3) THEN
         D1MACH = 1.1102230D-16
      ELSEIF (I.EQ.4) THEN
         D1MACH = 2.220446D-16
      ELSEIF (I.EQ.5) THEN
         D1MACH = 0.301029995663981195D+00
      ELSE
         D1MACH = 0.0D+00
      ENDIF
      RETURN
